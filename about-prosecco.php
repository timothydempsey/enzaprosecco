  <h1>ABOUT PROSECCO</h1>
<p>Known for its fresh and aromatic taste, Prosecco captures the romance and style of Italy. Made from Glera grapes, DOC Prosecco is produced in the regions of Veneto and Friuli Venezia Giulia in Northern Italy. Since 2009, the name “Prosecco” has been regulated under DOC law and only wines produced in these regions may be referred to as “Prosecco.”</p>

<p>Using the Charmat method, Prosecco goes through secondary fermentation in stainless steel tanks rather than the bottle. This gives Prosecco its distinctive light, fresh aromatic taste for which it has become famous. It also differentiates Prosecco from other sparkling wines such as Champagne, where secondary fermentation takes place in the bottle. In recent years, as production and viticulture techniques advanced, Proseccos have become finer and more complex.</p>

<p>Like Champagne, Prosecco is traditionally served chilled and enjoyed on its own as an aperitif. As a result, it has evolved into a renowned sparkling wine appreciated for its refreshing taste profile. However, in recent years, Prosecco’s versatility has been recognized. For one, it can be paired with a variety of foods. Prosecco’s balance of acidity and sweetness makes it a great accompaniment to seafood or pasta dishes. Also, it can be used to create delicious cocktails, like Venice’s most famous cocktail, the Bellini. Just combine Prosecco and fresh peach juice in a champagne flute and enjoy! So, when choosing the perfect wine to serve at your next get together or to relax with friends after a long day, choose Prosecco.</p>
